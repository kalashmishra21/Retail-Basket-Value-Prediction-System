"""
Django URL Configuration for API Endpoints

This module defines all REST API routes for the application:
- Authentication: register, login, logout
- User Management: profile, API keys, notifications
- Data Operations: dataset upload, prediction CRUD
- Analytics: metrics, trends, explainability, visualization

All endpoints require authentication except register/login.
Uses Django REST Framework's DefaultRouter for viewset routing.
"""

from django.urls import path, include
from rest_framework.routers import DefaultRouter
from . import views

# Router Configuration for ViewSets
# Automatically generates CRUD routes for datasets and predictions
# Creates: list, create, retrieve, update, partial_update, destroy endpoints
# Accessible at /api/datasets/ and /api/predictions/
router = DefaultRouter()
router.register(r'datasets', views.DatasetViewSet, basename='dataset')
router.register(r'predictions', views.PredictionViewSet, basename='prediction')

urlpatterns = [
    # Authentication Endpoints
    # POST /api/auth/register/ - Create new user account
    # POST /api/auth/login/ - Authenticate and get token
    # POST /api/auth/logout/ - Destroy session (requires auth)
    path('auth/register/', views.register_user, name='register'),
    path('auth/login/', views.login_user, name='login'),
    path('auth/logout/', views.logout_user, name='logout'),
    
    # User Profile Management Endpoints
    # GET/PUT /api/user/profile/ - View/update user info
    # POST /api/user/generate-api-key/ - Generate new API key
    # GET/PUT /api/user/notifications/ - Manage notification settings
    path('user/profile/', views.user_profile, name='user-profile'),
    path('user/generate-api-key/', views.generate_api_key, name='generate-api-key'),
    path('user/notifications/', views.notification_settings, name='notification-settings'),
    
    # Metrics and Analytics Endpoints
    # All return real-time calculated data from database
    # No dummy values - all metrics computed from actual predictions
    # Used by Dashboard, Metrics, and Visualization pages
    path('metrics/latest/', views.calculate_metrics, name='calculate-metrics'),
    path('metrics/summary/', views.metrics_summary, name='metrics-summary'),
    path('metrics/timeseries/', views.metrics_timeseries, name='metrics-timeseries'),
    path('metrics/snapshots/', views.metrics_snapshots, name='metrics-snapshots'),
    path('metrics/trends/', views.get_trends, name='metrics-trends'),

    # Explainability Endpoint
    # GET /api/explainability/<prediction_id>/ - Get feature importance
    # Returns deterministic feature rankings stored at prediction creation
    # Categories: Primary Driver, Supporting Factor, Insignificant
    path('explainability/<str:prediction_id>/', views.get_explainability, name='explainability'),
    
    # Visualization Data Endpoints
    # Provide chart-ready data for Visualization page
    # All calculations from real database records
    # Includes scatter plots, error distributions, category analysis
    path('visualization/scatter/', views.visualization_scatter, name='visualization-scatter'),
    path('visualization/error-distribution/', views.visualization_error_distribution, name='visualization-error-distribution'),
    path('visualization/category-analysis/', views.visualization_category_analysis, name='visualization-category-analysis'),
    path('visualization/summary/', views.visualization_summary, name='visualization-summary'),
    
    # Router URLs - ViewSet CRUD Operations
    # Includes all dataset and prediction management endpoints
    # Auto-generated by Django REST Framework router
    path('', include(router.urls)),
]
